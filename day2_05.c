#include <stdio.h>

int main()
{
    char c1 = 'A', c2 = 'a';

    printf("c1 ^ 0x20 = %c\n", c1 ^ 0x20);
    printf("c2 ^ 0x20 = %c\n", c2 ^ 0x20);

    // 위의 코드와 아래의 코드는 동의어다.
    // 32를 16진수로 표현하면 0x20이 된다.

    printf("c1 ^ 32 = %c\n", c1 ^ 32);
    printf("c2 ^ 32 = %c\n", c2 ^ 32);

    // 'a'  = 97 -> 110 0001
    // 'A'  = 65 -> 100 0001
    // 0x20 = 32 -> 010 0000

    // 'A'  = 65 -> 100 0001
    // 0x20 = 32 -> 010 0000 xor
    // ---------------------
    //              110 0001 -> 'a'

    // 'a'  = 97 -> 110 0001
    // 0x20 = 32 -> 010 0000 xor
    // ---------------------
    //              100 0001 -> 'A'

    return 0;
}

